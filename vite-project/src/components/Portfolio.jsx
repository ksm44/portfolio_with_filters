export default class Portfolio extends React.Component{
    state = {
        selectedFilter: "All"
    }
    
    
    filterItems = (filter) => { this.setState( {selectedFilter: filter} )};
    
    render(){
        const filters = ["All", ...new Set(portfolioCards.map(card => card.category))];
        
        const filtered = this.state.selectedFilter === "All" 
            ? portfolioCards
            : portfolioCards.filter(card => card.category === this.state.selectedFilter)  
        
        return(
            <div className="block">
                <Toolbar
                    filters = {filters}
                    selected = {this.state.selectedFilter}
                    onSelectFilter = {(filter) => { console.log(filter); this.filterItems(filter) }}/>
                <ProjectList projects = {filtered}/>
            </div>
        );
    }
}

